    # 删除旧索引
    if es.indices.exists(index=INDEX_NAME):
        es.indices.delete(index=INDEX_NAME)
        print(f"🗑️  已删除旧索引: {INDEX_NAME}")

    # 创建索引
    mapping = {
        "mappings": {
            "properties": {
                "title": {"type": "text", "analyzer": "ik_max_word", "search_analyzer": "ik_smart"},
                "content": {"type": "text", "analyzer": "ik_smart"},
                "category": {"type": "keyword"},
                "tags": {"type": "keyword"},
                "keywords": {"type": "text", "analyzer": "ik_max_word"},
                "priority": {"type": "integer"},
                "viewCount": {"type": "integer"},
                "status": {"type": "integer"},
                "createTime": {"type": "date"},
                "updateTime": {"type": "date"},
                "createBy": {"type": "keyword"}
            }
        }
    }
    es.indices.create(index=INDEX_NAME, body=mapping)
    print(f"📝 已创建索引: {INDEX_NAME}")

    # 导入数据
    articles = get_knowledge_data()
    print(f"\n📤 开始导入 {len(articles)} 条数据...")

    for i, article in enumerate(articles, 1):
        es.index(index=INDEX_NAME, body=article)
        print(f"   [{i}/{len(articles)}] {article['title']}")

